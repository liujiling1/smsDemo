package com.yjkj.sms.config.shiro.dto;

import java.io.Serializable;
import java.util.Date;

public class SysRole implements Serializable {
	private static final long serialVersionUID = 2385101929457196281L;
	private Integer id;
    private String roleId;
    private String des;
    private String menus;
    private String menuTypes;
    private Integer updaterId;
    private Date updateTime;
    private Integer createrId;
    private Date createTime;
    private byte del;
    
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getRoleId() {
		return roleId;
	}
	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}
	public String getDes() {
		return des;
	}
	public void setDes(String des) {
		this.des = des;
	}
	public String getMenus() {
		return menus;
	}
	public void setMenus(String menus) {
		this.menus = menus;
	}
	public String getMenuTypes() {
		return menuTypes;
	}
	public void setMenuTypes(String menuTypes) {
		this.menuTypes = menuTypes;
	}
	public Integer getUpdaterId() {
		return updaterId;
	}
	public void setUpdaterId(Integer updaterId) {
		this.updaterId = updaterId;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	public Integer getCreaterId() {
		return createrId;
	}
	public void setCreaterId(Integer createrId) {
		this.createrId = createrId;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public byte getDel() {
		return del;
	}
	public void setDel(byte del) {
		this.del = del;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	@Override
	public String toString() {
		return "SysRole [id=" + id + ", roleId=" + roleId + ", des=" + des + ", menus=" + menus + ", menuTypes="
				+ menuTypes + ", updaterId=" + updaterId + ", updateTime=" + updateTime + ", createrId=" + createrId
				+ ", createTime=" + createTime + ", del=" + del + "]";
	}
	
}